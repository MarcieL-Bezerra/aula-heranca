package Model;

public class Pessoa {
    private String nome;
    private String email;
    private String cpf;
    
    
	public String imprimeNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String imprimeEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

    
    

}
